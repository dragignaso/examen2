	<p>Louise Cooper (29 de mayo de 1952 - 21 de octubre de 2009) fue una escritora inglesa de literatura fantástica. Comenzó escribiendo en el colegio, espoleada su imaginación por los cuentos de Hans Christian Andersen, los hermanos Grimm y la mitología, aunque Michael Moorcock fue quien le descubrió, a partir de Stormbringer, el mundo de la literatura fantástica.

	Su primer gran éxito se produjo en 1984 cuando amplió a una trilogía un libro que pasó discretamente por los estantes: Lord of No Time (1977), que pasó a ser la trilogía de El señor del tiempo. Posteriormente, ha publicado las series Índigo, La puerta del caos entre otras.</p>

